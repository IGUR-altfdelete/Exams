#include <unistd.h>

int	main(int ac, char **av)
{
	const char	*s1;
	const char	*s2;
	int			i;

	i = 0;
	if (ac == 3)
	{
		while (s1[i])
		{
			while (*s2 && *s2 != s1[i])
				s2++;
			if (!*s2)
				break ;
			i++;
			s2++;
		}
		if (!s1[i])
			write(1, s1, i);
	}
	write(1, "\n", 1);
	return (0);
}
